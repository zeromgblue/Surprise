export async function render(container, data, config) {
    container.style.maxWidth = "100%";
    container.style.padding = "0";
    container.style.height = "100vh";
    container.style.display = "flex";
    container.style.alignItems = "center";
    container.style.justifyContent = "center";
    container.style.background = "#000"; 
    document.body.style.margin = "0";
    document.body.style.overflow = "hidden";

    await loadScript('https://cdnjs.cloudflare.com/ajax/libs/gsap/3.12.2/gsap.min.js');

    container.innerHTML = `
        <style>
            @import url('https://fonts.googleapis.com/css2?family=Orbitron:wght@700&family=Chakra+Petch:wght@300;600&display=swap');
            
            .plasma-scene {
                position: relative; width: 100vw; height: 100vh;
                display: flex; flex-direction: column; align-items: center; justify-content: center;
                overflow: hidden; background: radial-gradient(circle, #0f172a, #000);
            }

            /* Container for electrical tendrils */
            .sphereBox {
                position: absolute; width: 400px; height: 400px; border-radius: 50%;
                border: 2px solid rgba(255,255,255,0.1); box-shadow: inset 0 0 50px rgba(56,189,248,0.2), 0 0 50px rgba(139,92,246,0.1);
                display: flex; align-items: center; justify-content: center; z-index: 10; cursor: crosshair;
                overflow: hidden; /* keep lightning inside */
            }

            .core {
                position: absolute; width: 40px; height: 40px; background: #fff; border-radius: 50%;
                box-shadow: 0 0 20px #e879f9, 0 0 40px #c084fc; z-index: 15;
            }

            .tendril {
                position: absolute; top: 200px; left: 200px; width: 2px; height: 150px;
                background: #c084fc; filter: drop-shadow(0 0 5px #e879f9) drop-shadow(0 0 10px #c084fc);
                transform-origin: top center; opacity: 0.8;
                /* Jagged look via repeating SVG pattern (simplified with border dashed) */
                border-left: 2px dashed #fdf4ff; mix-blend-mode: screen;
            }

            .hint-text { position: absolute; bottom: 15vh; color: #e879f9; font-family: 'Chakra Petch', sans-serif; font-size: 1.2rem; letter-spacing: 2px; text-shadow: 0 0 10px #c084fc; animation: pulse 1s infinite alternate; font-weight: 300; z-index: 20; pointer-events: none;}
            @keyframes pulse { 0%{opacity:0.3;} 100%{opacity:1;} }

            /* Hacker/Sci-Fi Message Overlay */
            .energy-msg {
                position: absolute; inset: 0; display: flex; flex-direction: column;
                align-items: center; justify-content: center; z-index: 50; opacity: 0; pointer-events: none;
                background: radial-gradient(circle, rgba(0,0,0,0.6), rgba(0,0,0,0.9)); backdrop-filter: blur(5px);
            }
            .m-head { font-family: 'Orbitron', sans-serif; font-size: 4rem; color: #a5b4fc; margin-bottom: 20px; font-weight: 700; text-shadow: 0 0 10px #6366f1, 0 0 20px #818cf8; letter-spacing: 5px; text-transform: uppercase;}
            .m-body { font-family: 'Chakra Petch', sans-serif; font-size: 1.4rem; color: #e0e7ff; line-height: 1.8; max-width: 600px; text-align: center; font-weight: 300; border-left: 5px solid #6366f1; border-right: 5px solid #6366f1; padding: 0 40px;}
            .m-foot { font-family: 'Orbitron', sans-serif; font-size: 1.2rem; color: #818cf8; margin-top: 40px; letter-spacing: 3px;}

        </style>

        <div class="plasma-scene" id="scene">
            <div class="hint-text" id="hint">แตะลูกแก้วเพื่อชาร์จพลังงาน</div>

            <div class="sphereBox" id="sBox">
                <div class="core" id="core"></div>
                <!-- Tendrils generated by JS -->
            </div>

            <div class="energy-msg" id="msg">
                 <div class="m-head">${escapeHtml(data.receiver)}</div>
                 <div class="m-body">${escapeHtml(data.message).replace(/\n/g, '<br>')}</div>
                 <div class="m-foot">ENERGY FROM: ${escapeHtml(data.sender)}</div>
            </div>
        </div>
    `;

    const hint = document.getElementById('hint');
    const msg = document.getElementById('msg');
    const sBox = document.getElementById('sBox');
    
    // Create random tendrils connecting core to edge
    const numTendrils = 12;
    const tendrils = [];
    
    for(let i=0; i<numTendrils; i++) {
        let t = document.createElement('div');
        t.className = 'tendril';
        sBox.appendChild(t);
        tendrils.push(t);
        
        let angle = (360 / numTendrils) * i;
        gsap.set(t, { rotation: angle });
        
        // Twitch animation
        gsap.to(t, {
            rotation: angle + (Math.random()-0.5)*40,
            opacity: 0.2 + Math.random()*0.8,
            duration: 0.1 + Math.random()*0.2,
            repeat: -1, yoyo: true, ease: "rough"
        });
    }

    let isOverload = false;

    // Interactive touching (pull tendrils to cursor)
    sBox.addEventListener('mousemove', (e) => {
        if(isOverload) return;
        const rect = sBox.getBoundingClientRect();
        const cx = rect.left + 200; // center x
        const cy = rect.top + 200;  // center y
        
        // redirect a few tendrils towards mouse
        let angleToMouse = Math.atan2(e.clientY - cy, e.clientX - cx) * 180 / Math.PI - 90;
        gsap.to(tendrils[0], { rotation: angleToMouse, duration: 0.1 });
        gsap.to(tendrils[1], { rotation: angleToMouse + 10, duration: 0.1 });
        gsap.to(tendrils[2], { rotation: angleToMouse - 10, duration: 0.1 });
    });

    sBox.addEventListener('click', () => {
        if(isOverload) return;
        isOverload = true;
        hint.style.display = 'none';

        const tl = gsap.timeline();

        // 1. Core expands massively, flashing white
        tl.to('.core', { scale: 30, background: "#fff", boxShadow: "0 0 100px #fff", duration: 0.8, ease: "power4.in" })
        
        // 2. The whole sphere container breaks layout boundaries to cover screen with light
          .to(sBox, { width: "200vw", height: "200vh", borderRadius: "0%", borderColor: "transparent", duration: 0.5 }, 0.5)
          
        // 3. Screen blacks out momentarily
          .to('.core', { opacity: 0, duration: 0.2 }, 1)
          
        // 4. Glitchy tech message reveals
          .to(msg, { opacity: 1, pointerEvents: 'auto', duration: 1 }, 1.5)
          .from('.m-head', { opacity: 0, x: -50, duration: 0.5, ease: "rough" }, 1.5)
          .from('.m-body', { opacity: 0, scaleY: 0.1, duration: 1, ease: "power2.out" }, 2);
    });
}

function escapeHtml(str) {
    if(!str) return '';
    return str.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;').replace(/'/g,'&#039;');
}

function loadScript(src) {
    return new Promise((resolve, reject) => {
        if (document.querySelector(`script[src="${src}"]`)) return resolve();
        const s = document.createElement('script');
        s.src = src;
        s.onload = resolve;
        s.onerror = reject;
        document.head.appendChild(s);
    });
}
